    <h2>What is season-php?</h2>

    <p>season-php is a web application framework designed to maximize productivity. Web frameworks provide the structure and bootstrap for building applications, so developers can focus on creating modern website.</p>

    <p>season-php is designed with a module-based MVC structure, so you can easily compose web pages. Common libraries and configurations are managed in an integrated folder called app, and each page is organized as an independent module, so you can develop intuitively.</p>

    <p>To start using season-php, first you need to set up the environment.</p>
